/**
 *  根据UIColor对象生成一张图片
 */
+ (UIImage *)createImageWithColor:(UIColor *)color size:(CGSize)size
{
    if(size.width == 0 || size.height == 0){
        size = CGSizeMake(1, 1);
    }
    CGRect rect = CGRectMake(0, 0, size.width, size.height);
    // 开启图形上下文
    UIGraphicsBeginImageContext(rect.size);
    // 获取当前的上下文
    CGContextRef context = UIGraphicsGetCurrentContext();
    // 填充颜色
    CGContextSetFillColorWithColor(context, [color CGColor]);
    // 填充整个区域
    CGContextFillRect(context, rect);
    
    UIImage *createImage = UIGraphicsGetImageFromCurrentImageContext();
    // 关闭图形上下文
    UIGraphicsEndImageContext();
    return createImage;
}

/**
 *  根据图片名称生成一张原始的图片，去掉系统的渲染
 */
+ (UIImage *)originImageWithName:(NSString *)imageName
{
    UIImage *image = [UIImage imageNamed:imageName];
    image = [image imageWithRenderingMode:UIImageRenderingModeAlwaysOriginal];
    return image;
}

/**
 *  返回一张圆形图片
 */
- (instancetype)circleImage
{
    // 开启图形上下文
    UIGraphicsBeginImageContext(self.size);
    // 获取当前的上下文
    CGContextRef ctx = UIGraphicsGetCurrentContext();
    // 矩形框
    CGRect rect = CGRectMake(0, 0, self.size.width, self.size.height);
    // 添加一个圆
    CGContextAddEllipseInRect(ctx, rect);
    // 裁剪
    CGContextClip(ctx);
    
    // 在圆画一张图片
    [self drawInRect:rect];
    
    UIImage *image = UIGraphicsGetImageFromCurrentImageContext();
    
    // 关闭图形上下文
    UIGraphicsEndImageContext();
    
    return image;
}

/**
 *  对原来的图片的大小进行处理
 */
+ (UIImage *)originImage:(UIImage *)image scaleToSize:(CGSize)size
{
    UIGraphicsBeginImageContext(size);
    [image drawInRect:CGRectMake(0,0, size.width, size.height)];
    UIImage *scaleImage = UIGraphicsGetImageFromCurrentImageContext();
    UIGraphicsEndImageContext();
    return scaleImage;
}

/**
 *  根据图片名返回一张能够自由拉伸的图片
 */
+ (UIImage *)resizedImage:(NSString *)name
{
    UIImage *image = [UIImage imageNamed:name];
    return [image stretchableImageWithLeftCapWidth:image.size.width * 0.5 topCapHeight:image.size.height * 0.5];
}

/**
 *  将图片（UIImage对象）按指定的压缩率进行base64转码
 */
+ (NSString *)imageTranscodeWithImage:(UIImage *)image compressionQuality:(CGFloat)compressionQuality
{
    NSData *imageData = UIImageJPEGRepresentation(image, compressionQuality);
    NSString *imageSignStr = [NSString stringWithFormat:@"data:image/jpeg;base64,%@", [imageData base64EncodedStringWithOptions:NSDataBase64Encoding64CharacterLineLength]];
    return imageSignStr;
}

/**
 *  将图片（UIImage对象）直接转换成字符串
 */
+ (NSString *)imageTransToStringWithImage:(UIImage *)image
{
    NSData *imageData = UIImageJPEGRepresentation(image, 1.0);
    NSString *imageStr = [[NSString alloc] initWithData:imageData encoding:NSUTF8StringEncoding];
    return imageStr;
}

